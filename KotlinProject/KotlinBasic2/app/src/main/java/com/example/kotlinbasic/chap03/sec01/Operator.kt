package com.example.kotlinbasic.chap03.sec01

fun main() {
    var a = 10; var b = 20

    println("a, b : ${a}, ${b}")
    println("a+b : ${a+b}")
    println("a.plus (b) : ${a.plus(b)}")
    println("a-b : ${a-b}")
    println("a.minus (b) : ${a.minus (b)}")
    println("axb : ${a*b}")
    println("a.times(b) : ${a.times(b)}")
    println("b/a : ${b/a}")
    println("b.div(a): ${b.div(a)}")
    println("b%a: ${b % a}")
    println("b.rem(a) : ${b.rem(a)}")
}